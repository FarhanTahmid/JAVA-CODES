    public TaskEditor() {
        initComponents();
        conn = connect.ConnectDb();
        jButton1.setIcon(new ImageIcon("src/resources/save.png"));
        jButton2.setIcon(new ImageIcon("src/resources/back.png"));
        if (Universal.confirm) {
            String x = "select Task from Tasks where Username='" + Universal.user + "' AND Task='" + Universal.topic + "'";
            try {
                pst = conn.prepareStatement(x);
                rs = pst.executeQuery();
                if (rs.next()) {
                    jTextField2.setText(rs.getString(1));
                    rs.close();
                    pst.close();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

    public void saveTask() {
        if (Universal.confirm) {
            try {
                String p = jTextField2.getText();
                SimpleDateFormat abc = new SimpleDateFormat("dd-MM-yyyy");
                String date = abc.format(jDateChooser1.getDate());
                String z = "Update Tasks Set Task='" + p + "'Deadline='" + date + "'where Username='" + Universal.user + "' AND Task='" + Universal.topic + "'";
                pst = conn.prepareStatement(z);
                pst.execute();
                Universal.confirm = false;
                JOptionPane.showMessageDialog(null, "Your Task is Updated.");
                setVisible(false);
                Tasks ob = new Tasks();
                ob.setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

        } else {
            try {
                String sql = "Insert into Tasks (Username,Task,Deadline) values (?,?,?)";
                pst = conn.prepareStatement(sql);
                pst.setString(1, Universal.user);
                pst.setString(2, jTextField2.getText());
                SimpleDateFormat abc = new SimpleDateFormat("dd-MM-yyyy");
                String date = abc.format(jDateChooser1.getDate());
                pst.setString(3, date);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Your Task is Saved.");
                setVisible(false);
                Tasks ob = new Tasks();
                ob.setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);

            }
        }
    }

