    public void delete() {
        int row = jTable1.getSelectedRow();
        String cell = jTable1.getModel().getValueAt(row, 0).toString();
        try {
            String sql = "Delete From Tasks where Task ='" + cell + "' AND Username='" + Universal.user + "'";
            pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Removed the Task");
            setVisible(false);
            Tasks ob = new Tasks();
            ob.setVisible(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    public void edit() {
        int row = jTable1.getSelectedRow();
        Universal.confirm = true;
        Universal.topic = jTable1.getModel().getValueAt(row, 0).toString();
    }

    public void setTable() {
        try {
            String x = "select Task, Deadline from Tasks";
            pst = conn.prepareStatement(x);
            rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

