    public void Search(){
        String a=jTextField1.getText();
        String x="select * from Tasks where ID='"+a+"'";
           try{
               pst=conn.prepareStatement(x);
               rs=pst.executeQuery();
               if(rs.next()){
               jTextField2.setText(rs.getString(2));
               jTextField3.setText(rs.getString(3));
               
               rs.close();
               pst.close();
               }
               else{
                   JOptionPane.showMessageDialog(null,"Incorrect Task ID");
               }
           }catch(Exception e){
               JOptionPane.showMessageDialog(null,e);
           }
            
    }
    public void update(){
    try{
            String a=jTextField1.getText();
            String p=jTextField2.getText();
            String q=jTextField3.getText();
            String z="Update Tasks Set Task='"+p+"',Deadline='"+q+"' Where ID='"+a+"'";
            pst=conn.prepareStatement(z);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Your Task is Updated.");
            setVisible(false);
            Tasks ob=new Tasks();
            ob.setVisible(true); 
    }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
    }
            
            
    }
    
    public void delete(){
    try{
        String a=jTextField1.getText();
        String f="Delete From Tasks Where ID='"+a+"'";
        pst=conn.prepareStatement(f);
        pst.execute();
        JOptionPane.showMessageDialog(null, "Your Task is Deleted.");
        setVisible(false);
        Tasks ob=new Tasks();
        ob.setVisible(true);        
    }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
    }
    }

